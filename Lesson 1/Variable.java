public class Variable {
	public static void main(String[] args) {
		byte cores = 4;
		short threads = 8;
		int ram = 16;
		long ssd = 512;
		float ghz = 2.6f;
		double mhz = 2592f;
		char intelCore = 'i';
		boolean goodComputer = true;

		System.out.println("Сведения о системе:");
		System.out.println("cores = " + cores);
		System.out.println("threads =" + threads);
		System.out.println("ram = " + ram);
		System.out.println("ssd = " + ssd);
		System.out.println("ghz = " + ghz);
		System.out.println("mhz = " + mhz);
		System.out.println("intelCore = " + intelCore);
		System.out.println("goodComputer = " + goodComputer);
	}
}